import Twosum as twosum
class Solution:
    def threeSum(self, nums: list[int]) -> list[list[int]]:
        b = list()
        length =len(nums)
        for i in range(length):
            a = list()
            a = twosum.twosum3(nums[i+1:],-nums[i])
            a.insert(0,i)
            b.append(a)
        return b

